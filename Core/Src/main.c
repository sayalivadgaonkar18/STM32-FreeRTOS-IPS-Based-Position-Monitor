/**
  * @brief  Function implementing the ProcessingTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartProcessingTask */
void StartProcessingTask(void *argument)
{
  /* USER CODE BEGIN 5 */
    float sin_diff;
    float cos_diff;

    float angle_rad;
    float electrical_angle;

    float mechanical_angle;

    static float previous_angle = 0;

    static uint8_t cycle_count = 0;

  /* Infinite loop */
  for(;;)
  {
      osSemaphoreAcquire(adcBinarySemHandle, osWaitForever);

      /* Differential signals */
      sin_diff = (float)((int16_t)adc_buffer[0] - (int16_t)adc_buffer[1]);

      cos_diff = (float)((int16_t)adc_buffer[2] - (int16_t)adc_buffer[3]);

      /* Electrical angle */
      angle_rad = atan2f(sin_diff, cos_diff);

      electrical_angle = angle_rad * 180.0f / M_PI;

      if(electrical_angle < 0.0f)
      {
          electrical_angle += 360.0f;
      }

      /* Detect forward wrap */
      if((previous_angle > 300.0f) && (electrical_angle < 60.0f))
      {
          cycle_count++;

          if(cycle_count >= 4)
          {
              cycle_count = 0;
          }
      }

      /* Detect backward wrap */
      if((previous_angle < 60.0f) && (electrical_angle > 300.0f))
      {
          if(cycle_count == 0)
          {
              cycle_count = 3;
          }
          else
          {
              cycle_count--;
          }
      }

      previous_angle = electrical_angle;

      /* Convert to full mechanical angle */
      mechanical_angle = electrical_angle + (cycle_count * 360.0f);

      /* Scale to 0-360 mechanical */
      mechanical_angle = mechanical_angle / 4.0f;

      /* Wrap final angle */
      if(mechanical_angle >= 360.0f)
      {
          mechanical_angle -= 360.0f;
      }

      /* Low-pass filter */
      filtered_angle = (0.9f * filtered_angle) + (0.1f * mechanical_angle);

      osMessageQueuePut(angleQueueHandle,
                        &filtered_angle,
                        0,
                        0);
  }
  /* USER CODE END 5 */
}

